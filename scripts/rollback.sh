#!/usr/bin/env bash
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# HIVE-R Rollback Script
# Usage: ./scripts/rollback.sh [service_name]
# Example: ./scripts/rollback.sh hive_app
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
set -euo pipefail

SERVICE="${1:-hive_app}"
HEALTH_URL="${HEALTH_URL:-http://localhost:3000/health}"
ROLLBACK_FILE="/tmp/hive-rollback-image"

echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
echo "  ğŸ”´ HIVE-R Rollback"
echo "  Service: ${SERVICE}"
echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"

# â”€â”€ Determine rollback target â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
if [ -f "${ROLLBACK_FILE}" ]; then
    ROLLBACK_IMAGE=$(cat "${ROLLBACK_FILE}")
    if [ -n "${ROLLBACK_IMAGE}" ] && [ "${ROLLBACK_IMAGE}" != "none" ]; then
        echo "â¬…ï¸ Rolling back to: ${ROLLBACK_IMAGE}"
        docker service update --image "${ROLLBACK_IMAGE}" "${SERVICE}"
    else
        echo "â¬…ï¸ Using Docker Swarm native rollback..."
        docker service rollback "${SERVICE}"
    fi
else
    echo "â¬…ï¸ No rollback file found, using Docker Swarm native rollback..."
    docker service rollback "${SERVICE}"
fi

# â”€â”€ Wait and verify â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
echo "â³ Waiting for rollback to stabilize..."
sleep 20

for i in 1 2 3 4 5; do
    HTTP_STATUS=$(curl -sf -o /dev/null -w "%{http_code}" "${HEALTH_URL}" 2>/dev/null || true)
    if [ "${HTTP_STATUS}" = "200" ]; then
        CURRENT=$(docker service inspect "${SERVICE}" \
            --format '{{.Spec.TaskTemplate.ContainerSpec.Image}}' 2>/dev/null || echo "unknown")
        echo ""
        echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
        echo "  âœ… Rollback complete"
        echo "  Running: ${CURRENT}"
        echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
        exit 0
    fi
    echo "â³ Attempt ${i}/5: status=${HTTP_STATUS}, retrying in 5s..."
    sleep 5
done

echo ""
echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
echo "  ğŸ”´ CRITICAL: Rollback health check failed!"
echo "  Manual intervention required."
echo "  Check: docker service ps ${SERVICE}"
echo "â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
